// ─── Helper lookups ──────────────────────────────────────────

export function getProjectBySlug(slug: string): Project | undefined {
  return projects.find((p) => p.slug === slug);
}

export function getDeveloperBySlug(slug: string): Developer | undefined {
  return developers.find((d) => d.slug === slug);
}

export function getProjectsByDeveloper(developerSlug: string): Project[] {
  const dev = getDeveloperBySlug(developerSlug);
  if (!dev) return [];
  return projects.filter((p) => p.developerId === dev.id);
}

export function getProjectsByDistrict(districtNumber: number): Project[] {
  return projects.filter((p) => p.districtNumber === districtNumber);
}

export function getDistrictByNumber(num: number): DistrictInfo | undefined {
  return districts.find((d) => d.number === num);
}
