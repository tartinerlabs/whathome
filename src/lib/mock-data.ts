function generatePsfTrend(basePsf: number, months: number): PsfDataPoint[] {
  const points: PsfDataPoint[] = [];
  const monthNames = [
    "Jan",
    "Feb",
    "Mar",
    "Apr",
    "May",
    "Jun",
    "Jul",
    "Aug",
    "Sep",
    "Oct",
    "Nov",
    "Dec",
  ];

  for (let i = 0; i < months; i++) {
    const monthIndex = (3 + i) % 12;
    const year = 2025 + Math.floor((3 + i) / 12);
    const variation = Math.sin(i * 0.8) * 40 + i * 8;
    points.push({
      month: `${monthNames[monthIndex]} ${year.toString().slice(-2)}`,
      psf: Math.round(basePsf + variation),
    });
  }
  return points;
}

