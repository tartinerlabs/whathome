// ─── Transactions ────────────────────────────────────────────

function generateTransactions(
  slug: string,
  name: string,
  district: number,
  basePsf: number,
  tenure: string,
  count: number,
): Transaction[] {
  const floors = ["01-05", "06-10", "11-15", "16-20", "21-25", "26-30"];
  const txns: Transaction[] = [];

  for (let i = 0; i < count; i++) {
    const floor = floors[i % floors.length];
    const unit = `#${String(Math.floor(i / 6) + 1).padStart(2, "0")}-${String((i % 8) + 1).padStart(2, "0")}`;
    const areaSqft = 500 + Math.floor(Math.random() * 800);
    const psfVariation = basePsf + Math.floor(Math.random() * 200) - 100;
    const month = String((i % 12) + 1).padStart(2, "0");
    const day = String((i % 28) + 1).padStart(2, "0");

    txns.push({
      id: `txn-${slug}-${i}`,
      projectSlug: slug,
      projectName: name,
      unitNumber: unit,
      floorRange: floor,
      areaSqft,
      transactedPrice: areaSqft * psfVariation,
      pricePsf: psfVariation,
      contractDate: `2025-${month}-${day}`,
      saleType: "new_sale",
      propertyType: "condo",
      district,
      tenure,
    });
  }
  return txns;
}

